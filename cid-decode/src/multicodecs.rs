use failure::err_msg;
use ipfs_resolver_common::Result;
use std::collections::HashMap;

pub struct Multicodec {
    name: &'static str,
    tag: Tag,
}

#[derive(PartialEq, Eq, Debug)]
pub enum Tag {
    Cid,
    Filecoin,
    Holochain,
    Ipld,
    Key,
    Libp2p,
    Multiaddr,
    Multiformat,
    Multihash,
    Namespace,
    Serialization,
    Softhash,
    Transport,
    Zeroxcert,
}

impl Multicodec {
    fn new(name: &'static str, tag: &str) -> Self {
        Self {
            name,
            tag: match tag {
                "cid" => Tag::Cid,
                "filecoin" => Tag::Filecoin,
                "holochain" => Tag::Holochain,
                "ipld" => Tag::Ipld,
                "key" => Tag::Key,
                "libp2p" => Tag::Libp2p,
                "multiaddr" => Tag::Multiaddr,
                "multiformat" => Tag::Multiformat,
                "multihash" => Tag::Multihash,
                "namespace" => Tag::Namespace,
                "serialization" => Tag::Serialization,
                "softhash" => Tag::Softhash,
                "transport" => Tag::Transport,
                "zeroxcert" => Tag::Zeroxcert,
                _ => panic!("Found unkown type in MULTICODECTABLE"),
            },
        }
    }
}

pub struct MulticodecTable {
    table: &'static HashMap<u64, Multicodec>,
}

impl MulticodecTable {
    pub fn get_name_with_checked_tag(&self, codec: u64, tag: Tag) -> Option<Result<&'static str>> {
        match self.table.get(&codec) {
            None => None,
            Some(mc) => {
                if mc.tag == tag {
                    Some(Ok(mc.name))
                } else {
                    Some(Err(err_msg(format!(
                        "Expected multicodec {} to have {:?}-tag, found {:?} tag",
                        mc.name, mc.tag, tag
                    ))))
                }
            }
        }
    }
}

